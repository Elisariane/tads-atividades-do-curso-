package entidade;

public class Estado {
	private int idest;
	private String nome;
	private String sigla;
	public int getIdest() {
		return idest;
	}
	public void setIdest(int idest) {
		this.idest = idest;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getSigla() {
		return sigla;
	}
	public void setSigla(String sigla) {
		this.sigla = sigla;
	}
	

}
